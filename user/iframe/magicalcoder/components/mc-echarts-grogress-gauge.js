function McEChartsGrogressGauge(){
    //___mcUtils参考mc-utils.js
    this.mcUtils = ___mcUtils;
    this.param = null;
    this.domClass = "mc-echarts-grogress-gauge";
}
/*务必带此render方法：否则组件不会被追踪渲染*/
McEChartsGrogressGauge.prototype.render = function(param){
    if(!this.mcUtils.ifEchartsContinueRenderInMagicalCoder(param,this.domClass)){
        return;
    }
    this.param = param;
    this.grogressGauge();
}

